    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:                                          //AQUI COLOCA O NOME DO BANCO, USU√ÅRIO E SENHA DO BANCO  
            Connection conexao = DriverManager.getConnection("jdbc:mysql://localhost:3306/projetoPersona","root","");
            String consultarSQL = "SELECT * FROM Persona";
           //AQUI FAZ A BUSCA NA TABELA POR NOME,ALTERANDO  SQL PARA CASO O CAMPO DE PESQUISA TENHA VALOR DIFERENTE DE VAZIO 
            if(!txtNome.getText().equals(""))
                consultarSQL = consultarSQL + " WHERE Nome LIKE ? ";
            PreparedStatement stmt = conexao.prepareStatement(consultarSQL);
            //AQUI ELE PASSA NOME DIGITADO PARA DENTRO DA CONEXAO
             if(!txtNome.getText().equals(""))
                stmt.setString(1, "%"+txtNome.getText()+"%");
            ResultSet salvaSQL = stmt.executeQuery();
            DefaultTableModel modelPersona = (DefaultTableModel) jtPersona.getModel();
            modelPersona.setNumRows(0);
            while(salvaSQL.next()){
                String[] linha = {salvaSQL.getString("IDPersona"),salvaSQL.getString("Nome"),salvaSQL.getString("Email"),salvaSQL.getString("Idade")};
                modelPersona.addRow(linha);
            }
            stmt.close();
            conexao.close();
        } catch (SQLException excecao) {
            JOptionPane.showMessageDialog(this,excecao);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        try {
            // TODO add your handling code here:
            Connection conexao = DriverManager.getConnection("jdbc:mysql://localhost:3306/projetoPersona","root","");
            String excluirSQL = "DELETE FROM Persona WHERE IDPersona = ?";
            PreparedStatement stmt = conexao.prepareStatement(excluirSQL);
            int linha =  this.jtPersona.getSelectedRow(); //vai pegar o valor da linha selecionada na tabela jtPersona
            stmt.setInt(1,Integer.parseInt(this.jtPersona.getValueAt(linha, 0).toString()));
            stmt.execute();
            stmt.close();
            conexao.close();
            DefaultTableModel modelPersona = (DefaultTableModel) jtPersona.getModel();
            modelPersona.removeRow(linha); //remove a linha selecionada da tabela
            JOptionPane.showMessageDialog(this,"Persona Exclu√≠da!");
            this.setClosable(true);
        } catch (SQLException excecao) {
           JOptionPane.showMessageDialog(this,excecao);
        }
        
    }//GEN-LAST:event_btExcluirActionPerformed

