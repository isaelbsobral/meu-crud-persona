    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
      
        try {
            // TODO add your handling code here:
           if (idAlteracao == 0){                           //AQUI COLOCA O NOME DO BANCO, USUÁRIO E SENHA DO BANCO
            Connection conexao = DriverManager.getConnection("jdbc:mysql://localhost:3306/projetoPersona","root","");
            String sqlPersona = "INSERT INTO persona(nome,email,idade)VALUES(?,?,?)";
            PreparedStatement stmt = conexao.prepareStatement(sqlPersona);
            stmt.setString(1,this.txtNome.getText());
            stmt.setString(2,this.txtEmail.getText());
            stmt.setInt(3, Integer.parseInt(this.txtIdade.getText()));
            stmt.execute();
            stmt.close();
            conexao.close();
        }else{
            Connection conexao = DriverManager.getConnection("jdbc:mysql://localhost:3306/projetoPersona","root","");    
            String editarSQL = "UPDATE persona SET nome = ?, email=?,idade=? WHERE  idPersona = ?" ;
            PreparedStatement stmt = conexao.prepareStatement(editarSQL);
            stmt.setString(1,this.txtNome.getText());
            stmt.setString(2,this.txtEmail.getText());
            stmt.setInt(3,Integer.parseInt(this.txtIdade.getText()));
            stmt.setInt(4,idAlteracao);
            stmt.execute();
            stmt.close();
            conexao.close();
            }
            JOptionPane.showMessageDialog(this, "Persona Cadastrada!");
            this.dispose();
        } catch (SQLException excecao) {
            JOptionPane.showMessageDialog(this,excecao);
        }
    }//GEN-LAST:event_btSalvarActionPerformed

