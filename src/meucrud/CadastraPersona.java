    public CadastraPersona(int idAlteracao){
        initComponents();
        this.idAlteracao = idAlteracao;
        try{
            Connection conexao = DriverManager.getConnection("jdbc:mysql://localhost:3306/projetoPersona","root","");
            String editarSQL = "SELECT * FROM Persona WHERE IDPersona = ?";
            PreparedStatement stmt = conexao.prepareStatement(editarSQL);
            stmt.setInt(1,idAlteracao);
            ResultSet salvaSQL = stmt.executeQuery();
            //AQUI VAI PEGAR OS VALORES E EXIBIR DENTRO DOS INPUTS PARA PODER EDITAR
            while(salvaSQL.next()){
                txtID.setText(salvaSQL.getString("IDPersona"));
                txtID.setEnabled(false); //não permite a edição do campo
                txtNome.setText(salvaSQL.getString("Nome"));
                txtEmail.setText(salvaSQL.getString("Email"));
                txtIdade.setText(salvaSQL.getString("Idade"));                
            }
            stmt.close();
            conexao.close();
        }catch(SQLException excecao){
            JOptionPane.showMessageDialog(this,excecao);
        }
    }

